function setup() {
  /* Set the canvas to be under the element #canvas*/
  const canvas = createCanvas(1500, 1500);
  canvas.parent(document.querySelector("#canvas"));

  /*Calculate the number of columns and rows */
  columns = floor(width / unitLength);
  rows = floor(height / unitLength);

  /*Making both currentBoard and nextBoard 2-dimensional matrix that has (columns * rows) boxes. */
  currentBoard = [];
  nextBoard = [];
  for (let i = 0; i < columns; i++) {
    currentBoard[i] = [];
    nextBoard[i] = [];
  }
  // Now both currentBoard and nextBoard are array of array of undefined values.
  init(); // Set the initial values of the currentBoard and nextBoard
}

function init() {
  // RULES: LONE DEATH
  let loneDeath = document.querySelector("#die-of-loneliness");
  if (loneDeath.value == 2) {
    loneDeathRules = 2;
    console.log("loneDeath is now 2");
  }
  if (loneDeath.value == 3) {
    loneDeathRules = 3;
    console.log("loneDeath is now 3");
  }
  if (loneDeath.value == 4) {
    loneDeathRules = 4;
    console.log("loneDeath is now 4");
  }

  // RULES: OVERPOPULATION
  let overpopDeath = document.querySelector("#die-of-overpopulation");
  if (overpopDeath.value == 3) {
    overPopulationRules = 3;
    console.log("overpop is now 3");
  }
  if (overpopDeath.value == 2) {
    overPopulationRules = 2;
    console.log("overpop is now 2");
  }
  if (overpopDeath.value == 4) {
    overPopulationRules = 4;
    console.log("overpop is now 4");
  }

  // RULES: REPRODUCTION
  let reproduction = document.querySelector("#reproduction");
  if (reproduction.value == 3) {
    reproductionRules = 3;
    console.log("reproduction is now 3");
  }
  if (reproduction.value == 2) {
    reproductionRules = 2;
    console.log("reproduction is now 2");
  }
  if (reproduction.value == 4) {
    reproductionRules = 4;
    console.log("reproduction is now 4");
  }

  for (let i = 0; i < columns; i++) {
    for (let j = 0; j < rows; j++) {
      currentBoard[i][j] = 0;
      nextBoard[i][j] = 0;
    }
  }
}

function draw() {
  console.log("draw()");
  let colorPick = saveState % 5;
  testColor = colorKey[colorPick];

  frameRate(speed);
  background(255);

  generate();
  for (let i = 0; i < columns; i++) {
    for (let j = 0; j < rows; j++) {
      if (currentBoard[i][j] == 1) {
        fill(testColor);
        aliveNumber += 1;
      } else {
        fill(255);
      }
      stroke(strokeColor);
      rect(i * unitLength, j * unitLength, unitLength, unitLength);
    }
  }

  document.querySelector(".alive-number").innerText = aliveNumber;
  document.querySelector(".died-number").innerText = diedNumber;
}

function generate() {
  aliveNumber = 0;
  //Loop over every single box on the board
  for (let x = 0; x < columns; x++) {
    for (let y = 0; y < rows; y++) {
      // Count all living members in the Moore neighborhood(8 boxes surrounding)
      let neighbors = 0;
      for (let i of [-1, 0, 1]) {
        for (let j of [-1, 0, 1]) {
          if (i == 0 && j == 0) {
            // the cell itself is not its own neighbor
            continue;
          }
          // The modulo operator is crucial for wrapping on the edge
          neighbors +=
            currentBoard[(x + i + columns) % columns][(y + j + rows) % rows];
        }
      }

      // Rules of Life
      if (currentBoard[x][y] == 1 && neighbors < loneDeathRules) {
        // Die of Loneliness
        nextBoard[x][y] = 0;
        diedNumber += 1;
      } else if (currentBoard[x][y] == 1 && neighbors > overPopulationRules) {
        // Die of Overpopulation
        nextBoard[x][y] = 0;
        diedNumber += 1;
      } else if (currentBoard[x][y] == 0 && neighbors == reproductionRules) {
        // New life due to Reproduction
        nextBoard[x][y] = 1;
        saveState += 1;
      } else {
        // Stasis
        nextBoard[x][y] = currentBoard[x][y];
      }
    }
  }

  // Swap the nextBoard to be the current Board
  [currentBoard, nextBoard] = [nextBoard, currentBoard];
}

function mouseDragged() {
  if (running == 0) {
    if (mouseX > unitLength * columns || mouseY > unitLength * rows) {
      return;
    }
    const x = Math.floor(mouseX / unitLength);
    const y = Math.floor(mouseY / unitLength);
    currentBoard[x][y] = 1;
    fill(boxColor);
    stroke(strokeColor);
    rect(x * unitLength, y * unitLength, unitLength, unitLength);
  } else return;
}

function mousePressed() {
  if (running == 0) {
    noLoop();
    mouseDragged();
  } else return false;
}

