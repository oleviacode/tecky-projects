document.addEventListener("keydown", keyDownHandler);

// let keyBoardSwitch = 0;
let mouseColumn = 0;
let mouseRow = 0;

function keyDownHandler(e) {
  noLoop();
  init();

  console.log("Enter keyboard mode");
  // keyBoardSwitch = 1;

  mover(e);

  for (let i = 0; i < columns; i++) {
    for (let j = 0; j < rows; j++) {
      if (currentBoard[i][j] == 1) {
        fill("black");
      } else if (currentBoard[i][j] == 3) {
        fill("green");
      } else {
        fill(255);
      }

      stroke(strokeColor);
      rect(i * unitLength, j * unitLength, unitLength, unitLength);
    }
  }

  return;
}

function mover(e) {
  currentBoard[mouseColumn][mouseRow] = 3;

  if (e.code == "KeyA") {
    mouseColumn -= 1;
    // currentBoard[mouseColumn][mouseRow] = 3;
    // mouseColumn += 1;
    // currentBoard[mouseColumn][mouseRow] = 0;
  } else if (e.code == "KeyW") {
    mouseRow -= 1;
  } else if (e.code == "KeyD") {
    mouseColumn += 1;
    // currentBoard[mouseColumn][mouseRow] = 3;
    // mouseColumn -= 1;
    // currentBoard[mouseColumn][mouseRow] = 0;
  } else if (e.code == "KeyX") {
    mouseRow += 1;
  } else if (e.code == "KeyS") {
    currentBoard[mouseColumn][mouseRow] = 1;
  } else if (e.code == "KeyQ") {
    currentBoard[mouseColumn][mouseRow] = 0;
    return;
  }
}
